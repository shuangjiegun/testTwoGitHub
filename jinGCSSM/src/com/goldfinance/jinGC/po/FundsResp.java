package com.goldfinance.jinGC.po;
/**
 * 
 * @author liuhui
 * 用户资金response  domain
 *
 */
public class FundsResp {
	/**
	 * 成功标志
	 */
	private String success_type;
	/**
	 * 交易账号
	 */
	private String trade_acco;
	/**
	 * 基金账号
	 */
	private String ta_acco;
	/**
	 * 当前余额
	 */
	private String current_balance;
	/**
	 * 可用余额
	 */
	private String enable_balance;
	/**
	 * 最后存入日期
	 */
	private String last_in_date;
	/**
	 * 总记录数
	 */
	private String total_count;
	/**
	 * 记录数
	 */
	private String rowcount;
	public String getSuccess_type() {
		return success_type;
	}
	public void setSuccess_type(String success_type) {
		this.success_type = success_type;
	}
	public String getTrade_acco() {
		return trade_acco;
	}
	public void setTrade_acco(String trade_acco) {
		this.trade_acco = trade_acco;
	}
	public String getTa_acco() {
		return ta_acco;
	}
	public void setTa_acco(String ta_acco) {
		this.ta_acco = ta_acco;
	}
	public String getCurrent_balance() {
		return current_balance;
	}
	public void setCurrent_balance(String current_balance) {
		this.current_balance = current_balance;
	}
	public String getEnable_balance() {
		return enable_balance;
	}
	public void setEnable_balance(String enable_balance) {
		this.enable_balance = enable_balance;
	}
	public String getLast_in_date() {
		return last_in_date;
	}
	public void setLast_in_date(String last_in_date) {
		this.last_in_date = last_in_date;
	}
	public String getTotal_count() {
		return total_count;
	}
	public void setTotal_count(String total_count) {
		this.total_count = total_count;
	}
	public String getRowcount() {
		return rowcount;
	}
	public void setRowcount(String rowcount) {
		this.rowcount = rowcount;
	}
	@Override
	public String toString() {
		return "Funds [success_type=" + success_type + ", trade_acco="
				+ trade_acco + ", ta_acco=" + ta_acco + ", current_balance="
				+ current_balance + ", enable_balance=" + enable_balance
				+ ", last_in_date=" + last_in_date + ", total_count="
				+ total_count + ", rowcount=" + rowcount + "]";
	}
	
	

}
