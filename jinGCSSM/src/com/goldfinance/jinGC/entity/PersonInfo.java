package com.goldfinance.jinGC.entity;
/**
 * 
 * @author liuhui
 * 个人资料中心
 *
 */
public class PersonInfo {

	private String client_name;//客户姓名
	private String id_kind_gb;//证件类别
	private String id_no;//证件号码
	
	private String address;//联系地址
	private String e_mail;//电子信箱
	private String mobile_tel;//手机号码
	private String ofund_risklevel;//基金风险等级
	private String trade_acco; //交易账号
	
	public String getTrade_acco() {
		return trade_acco;
	}
	public void setTrade_acco(String trade_acco) {
		this.trade_acco = trade_acco;
	}
	public String getClient_name() {
		return client_name;
	}
	public void setClient_name(String client_name) {
		this.client_name = client_name;
	}
	public String getId_kind_gb() {
		return id_kind_gb;
	}
	public void setId_kind_gb(String id_kind_gb) {
		this.id_kind_gb = id_kind_gb;
	}
	public String getId_no() {
		return id_no;
	}
	public void setId_no(String id_no) {
		this.id_no = id_no;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getE_mail() {
		return e_mail;
	}
	public void setE_mail(String e_mail) {
		this.e_mail = e_mail;
	}
	public String getMobile_tel() {
		return mobile_tel;
	}
	public void setMobile_tel(String mobile_tel) {
		this.mobile_tel = mobile_tel;
	}
	public String getOfund_risklevel() {
		return ofund_risklevel;
	}
	public void setOfund_risklevel(String ofund_risklevel) {
		this.ofund_risklevel = ofund_risklevel;
	}
	@Override
	public String toString() {
		return "PersonInfo [client_name=" + client_name + ", id_kind_gb="
				+ id_kind_gb + ", id_no=" + id_no + ", address=" + address
				+ ", e_mail=" + e_mail + ", mobile_tel=" + mobile_tel
				+ ", ofund_risklevel=" + ofund_risklevel + ", trade_acco="
				+ trade_acco + "]";
	}
	
}
