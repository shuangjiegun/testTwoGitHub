package com.goldfinance.jinGC.entity;

public class TextMessage {
	private	String	bankCode	;
	private	String	bankAccount	;
	private  String  userName;
	private	String	id_kind_gb	;
	private	String	id_no	;
	private	String	mobile_tel	;
	private String sign_contract_no; //签约协议号，之后传给开户时的资金卡号fund_card
	private String status;
	private String verifycode;
	private String flag;  //1代表第一步发短信验证码，2代表第二步验证
	private String original_serial_no;   //第一遍调用接口时的流水号
	public String getBankCode() {
		return bankCode;
	}
	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}
	public String getBankAccount() {
		return bankAccount;
	}
	public void setBankAccount(String bankAccount) {
		this.bankAccount = bankAccount;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getId_kind_gb() {
		return id_kind_gb;
	}
	public void setId_kind_gb(String id_kind_gb) {
		this.id_kind_gb = id_kind_gb;
	}
	public String getId_no() {
		return id_no;
	}
	public void setId_no(String id_no) {
		this.id_no = id_no;
	}
	public String getMobile_tel() {
		return mobile_tel;
	}
	public void setMobile_tel(String mobile_tel) {
		this.mobile_tel = mobile_tel;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getVerifycode() {
		return verifycode;
	}
	public void setVerifycode(String verifycode) {
		this.verifycode = verifycode;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public String getOriginal_serial_no() {
		return original_serial_no;
	}
	public void setOriginal_serial_no(String original_serial_no) {
		this.original_serial_no = original_serial_no;
	}
	public String getSign_contract_no() {
		return sign_contract_no;
	}
	public void setSign_contract_no(String sign_contract_no) {
		this.sign_contract_no = sign_contract_no;
	}
	@Override
	public String toString() {
		return "TextMessage [bankCode=" + bankCode + ", bankAccount="
				+ bankAccount + ", userName=" + userName + ", id_kind_gb="
				+ id_kind_gb + ", id_no=" + id_no + ", mobile_tel="
				+ mobile_tel + ", sign_contract_no=" + sign_contract_no
				+ ", status=" + status + ", verifycode=" + verifycode
				+ ", flag=" + flag + ", original_serial_no="
				+ original_serial_no + "]";
	}
	
}
