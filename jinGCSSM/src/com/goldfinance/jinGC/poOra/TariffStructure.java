package com.goldfinance.jinGC.poOra;

import java.util.Date;

public class TariffStructure {
	private int innerCode;
	private int chargeRateType;  //费率类别
	private float minimumChargeRate; //费率最低值
	private float maximumChargeRate; //费率最高值
	private String chargeRateDesciption; //费率描述       主要得到它
	private String intervalDescription; //区间描述			  主要得到它
	private String weChatRate;  //微信购买费率
	private Date excuteDate; //执行日期
	public int getInnerCode() {
		return innerCode;
	}
	public void setInnerCode(int innerCode) {
		this.innerCode = innerCode;
	}
	public int getChargeRateType() {
		return chargeRateType;
	}
	public void setChargeRateType(int chargeRateType) {
		this.chargeRateType = chargeRateType;
	}
	public float getMinimumChargeRate() {
		return minimumChargeRate;
	}
	public void setMinimumChargeRate(float minimumChargeRate) {
		this.minimumChargeRate = minimumChargeRate;
	}
	public float getMaximumChargeRate() {
		return maximumChargeRate;
	}
	public void setMaximumChargeRate(float maximumChargeRate) {
		this.maximumChargeRate = maximumChargeRate;
	}
	public String getChargeRateDesciption() {
		return chargeRateDesciption;
	}
	public void setChargeRateDesciption(String chargeRateDesciption) {
		this.chargeRateDesciption = chargeRateDesciption;
	}
	public String getIntervalDescription() {
		return intervalDescription;
	}
	public void setIntervalDescription(String intervalDescription) {
		this.intervalDescription = intervalDescription;
	}
	public Date getExcuteDate() {
		return excuteDate;
	}
	public void setExcuteDate(Date excuteDate) {
		this.excuteDate = excuteDate;
	}
	
	public String getWeChatRate() {
		return weChatRate;
	}
	public void setWeChatRate(String weChatRate) {
		this.weChatRate = weChatRate;
	}
	@Override
	public String toString() {
		return "TariffStructure [innerCode=" + innerCode + ", chargeRateType="
				+ chargeRateType + ", minimumChargeRate=" + minimumChargeRate
				+ ", maximumChargeRate=" + maximumChargeRate
				+ ", chargeRateDesciption=" + chargeRateDesciption
				+ ", intervalDescription=" + intervalDescription
				+ ", weChatRate=" + weChatRate + ", excuteDate=" + excuteDate
				+ "]";
	}

}
